export const verifyOTP = async (req, res) => {
  try {
    const { orderId, shopOrderId, otp } = req.body;

    if (!orderId || !shopOrderId || !otp) {
      return res.status(400).json({ message: "Missing required fields" });
    }

    const order = await Order.findById(orderId)
      .populate("user", "socketId")
      .populate("shopOrders.owner", "socketId");

    if (!order) {
      return res.status(404).json({ message: "Order not found" });
    }

    const shopOrder = order.shopOrders.id(shopOrderId);
    if (!shopOrder) {
      return res.status(404).json({ message: "Shop order not found" });
    }

    // üîê validate OTP
    if (
      !shopOrder.deliveryOtp ||
      shopOrder.deliveryOtp !== otp ||
      !shopOrder.otpExpires ||
      shopOrder.otpExpires < Date.now()
    ) {
      return res.status(400).json({ message: "Invalid or expired OTP" });
    }

    // ‚úÖ mark delivered
    shopOrder.status = "delivered";
    shopOrder.deliveredAt = new Date();
    shopOrder.deliveryOtp = null;
    shopOrder.otpExpires = null;

    await order.save();

    // üîî real-time socket updates
    const io = req.app.get("io");

    if (io) {
      if (order.user?.socketId) {
        io.to(order.user.socketId).emit("update-Status", {
          orderId: order._id,
          shopId: shopOrder.shop,
          status: "delivered",
        });
      }

      if (shopOrder.owner?.socketId) {
        io.to(shopOrder.owner.socketId).emit("update-Status", {
          orderId: order._id,
          shopId: shopOrder.shop,
          status: "delivered",
        });
      }
    }

    // üßπ cleanup assignment
    await DeliveryAssignment.deleteOne({
      shopOrderId: shopOrder._id,
      order: order._id,
      assignedTo: shopOrder.assignedDeliveryBoy,
    });

    return res.status(200).json({
      message: "Order delivered successfully",
    });
  } catch (error) {
    console.error("Verify delivery OTP error:", error);
    return res.status(500).json({
      message: "Failed to verify delivery OTP",
    });
  }
};

export const getTodayDelivery = async (req, res) => {
  try {
    const deliveryBoyId = req.userId;
    const startsofDay = new Date();
    startsofDay.setHours(0, 0, 0, 0);

    const orders = await Order.find({
      "shopOrders.assignedDeliveryBoy": deliveryBoyId,
      "shopOrders.status": "delivered",
      "shopOrders.deliveredAt": { $gte: startsofDay },
    }).lean();

    let todaysDeliveries = [];

    orders.forEach((order) => {
      order.shopOrders.forEach((shopOrder) => {
        if (
          shopOrder.assignedDeliveryBoy == deliveryBoyId &&
          shopOrder.status == "delivered" &&
          shopOrder.deliveredAt &&
          shopOrder.deliveredAt >= startsofDay
        ) {
          todaysDeliveries.push(shopOrder);
        }
      });
    });
    let stats = {};

    todaysDeliveries.forEach((shopOrder) => {
      const hour = new Date(shopOrder.deliveredAt).getHours();
      stats[hour] = (stats[hour] || 0) + 1;
    });

    let formattedStats = Object.keys(stats).map((hour) => ({
      hour: parseInt(hour),
      count: stats[hour],
    }));

    formattedStats.sort((a, b) => a.hour - b.hour);

    return res.status(200).json(formattedStats);
  } catch (error) {
    return res.status(500).json({ message: `today delivery error ${error}` });
  }
};
